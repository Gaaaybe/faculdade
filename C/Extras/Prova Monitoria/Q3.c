/*
Q3) Quais os tipos de dados em C e sua string de formato?

Os tipos de dados mais basicos de C sâo:

void: Representa a falta de tipo ex->  , não tem formato.
int: Para inteiros ex -> 1, 2, 3...(etc). Seu formato dentro do codigo é %d.
float: Para numeros flutuantes(decimais) ex-> 1.6, 5.5, 93.996...(etc). Seu formato dentro do codigo é %f.
double: Para numeros tambem flutuantes(decimais) so que com dupla precisão. Seu formato dentro do codigo é %lf.
char: Sao caracteres como letras e simbolos ex-> sdfvas :). Seu formato é %s.

*/